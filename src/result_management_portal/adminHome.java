        // TODO add your handling code here:
        String course = (String)jComboBox1.getSelectedItem();
        String branchname = (String)jComboBox2.getSelectedItem();
        String gender = (String)jComboBox3.getSelectedItem();
        String rollnumber = jTextField1.getText();
        String name = jTextField2.getText();
        String fathername = jTextField3.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","yash@11");
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(Roll_Number,Course,Branch_Name,Name,Gender,Father_Name) values('"+rollnumber+"','"+course+"','"+branchname+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null,"Data saved successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
