    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String phy = jTextField2.getText();
        String math = jTextField3.getText();
        String chem = jTextField4.getText();
        String hin = jTextField5.getText();
        String comp = jTextField6.getText();
        String eng = jTextField7.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","yash@11");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("Select * from student where Roll_Number='"+roll+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(Roll_Number, Physics, Mathmatics, Chemistry, Hindi, Computer,English) values ('"+roll+"','"+phy+"','"+math+"','"+chem+"','"+hin+"','"+comp+"','"+eng+"')");
                JOptionPane.showMessageDialog(null,"Marks added successfully!");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Entered roll number is not registered!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

